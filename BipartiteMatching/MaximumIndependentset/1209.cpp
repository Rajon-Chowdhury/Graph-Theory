const int MAX = 1e3+5;
vector < int > adj[MAX];
bool vis[MAX];
int match[MAX];

bool DFS(int src)
{
    for(int i=0;i<adj[src].size();i++)
    {
        int x = adj[src][i];

        if(vis[x])
            continue;

        vis[x] = 1;

        if(match[x] == -1 || DFS(match[x]))
        {
            match[x] = src;
            match[src] = x;
            return 1;
        }
    }
    return 0;
}
int BPM(int n)
{
    int ans = 0;
    for(int i=0;i<n;i++)
    {
        memset(vis,0,sizeof vis);
        if(~match[i]||!DFS(i))
            ans++;
    }
    return ans;
}
void init()
{
    for(int i=0;i<MAX;i++)
        adj[i].clear();
    memset(match,-1,sizeof match);
}

int main()
{


    int t,cas=1,n,m,p;

    cin >> t;

    while(t--)
    {
        init();

        cin >> n >> m >> p;

        string like[p+2] , dislike[p+2];

        for(int i=0;i<p;i++)
            cin >> like[i] >> dislike[i];

        for(int i=0;i<p;i++)
        {
            for(int j=0;j<p;j++)
            {
                if(like[i] == dislike[j] || dislike[i] == like[j])
                    adj[i].push_back(j);
            }
        }
        cout << "Case " << cas++ << ": " << BPM(p) << endl;
    }
}
